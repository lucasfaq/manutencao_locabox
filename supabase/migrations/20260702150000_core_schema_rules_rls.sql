        from public.atendimentos_ordens ao
        where ao.id_atendimento = movimentacoes_estoque.id_atendimento
          and ao.criado_por = (select auth.uid())
      )
      or exists (
        select 1
        from public.equipes e
        where e.id_atendimento = movimentacoes_estoque.id_atendimento
          and e.id_colaborador = private.current_id_colaborador()
      )
    )
  )
);
comment on policy "movimentacoes_estoque_insert_baixa_ou_gestor" on public.movimentacoes_estoque
  is 'Tecnico so baixa material via atendimento em que participa; gestor pode fazer ajustes completos.';

