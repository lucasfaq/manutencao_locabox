create or replace view public.vw_atendimentos_por_projeto
with (security_invoker = true)
as
select
  c.id_contrato,
  c.numero_contrato,
  pr.id_projeto,
  pr.nome as projeto_nome,
  ui.id_unidade,
  ui.codigo as unidade_codigo,
  om.id_ordem,
  om.protocolo,
  ao.id_atendimento,
  ao.status_codigo as status_atendimento,
  ao.data_abertura,
  em.id_material,
  em.descricao as material_descricao,
  me.quantidade as material_quantidade,
  col.id_colaborador,
  col.nome as colaborador_nome,
  ter.id_terceira,
  ter.nome as terceirizado_nome
from public.atendimentos_ordens ao
join public.ordens_manutencao om on om.id_ordem = ao.id_ordem
join public.unidades_instaladas ui on ui.id_unidade = om.id_unidade
join public.projetos pr on pr.id_projeto = ui.id_projeto
join public.contratos c on c.id_contrato = pr.id_contrato
left join public.movimentacoes_estoque me on me.id_atendimento = ao.id_atendimento
left join public.estoque_materiais em on em.id_material = me.id_material
left join public.equipes eq on eq.id_atendimento = ao.id_atendimento
left join public.colaboradores col on col.id_colaborador = eq.id_colaborador
left join public.terceirizados ter on ter.id_terceira = eq.id_terceira
where private.is_gestor((select auth.uid()));

