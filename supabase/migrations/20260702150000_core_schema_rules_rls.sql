create or replace function private.ensure_ordem_contrato_ativo()
returns trigger
language plpgsql
security definer
set search_path = ''
as $$
begin
  if not exists (
    select 1
    from public.unidades_instaladas ui
    join public.projetos p on p.id_projeto = ui.id_projeto
    join public.contratos c on c.id_contrato = p.id_contrato
    where ui.id_unidade = new.id_unidade
      and ui.ativo is true
      and p.ativo is true
      and c.ativo is true
      and c.status_codigo = 'ativo'
  ) then
    raise exception 'OS so pode ser criada para unidade vinculada a contrato ativo';
  end if;

  new.criado_por = coalesce(new.criado_por, auth.uid());
  return new;
end;
$$;

create or replace function private.load_pendencias_atendimento()
returns trigger
language plpgsql
security definer
set search_path = ''
as $$
begin
  insert into public.atendimentos_executados (id_atendimento, id_pendencia_ordem, status_codigo)
  select new.id_atendimento, pom.id_pendencia_ordem, 'aberta'
  from public.pendencias_ordem_manutencao pom
  where pom.id_ordem = new.id_ordem
    and pom.status_codigo <> 'concluida'
  on conflict (id_atendimento, id_pendencia_ordem) do nothing;

  return new;
end;
$$;

create or replace function private.recalcular_status_ordem(target_id_ordem integer)
returns void
language plpgsql
security definer
set search_path = ''
as $$
declare
  total_pendencias integer;
  concluidas integer;
  tratadas integer;
  next_status text;
begin
  select
    count(*),
    count(*) filter (where pom.status_codigo = 'concluida'),
    count(*) filter (
      where pom.status_codigo = 'concluida'
         or exists (
           select 1
           from public.atendimentos_executados ae
           where ae.id_pendencia_ordem = pom.id_pendencia_ordem
             and ae.status_codigo <> 'aberta'
         )
    )
  into total_pendencias, concluidas, tratadas
  from public.pendencias_ordem_manutencao pom
  where pom.id_ordem = target_id_ordem;

  if total_pendencias > 0 and concluidas = total_pendencias then
    next_status := 'concluida';
  elsif tratadas > 0 then
    next_status := 'em_andamento';
  else
    next_status := 'aberta';
  end if;

  update public.ordens_manutencao
  set status_codigo = next_status,
      data_conclusao = case
        when next_status = 'concluida' then coalesce(data_conclusao, now())
        else null
      end,
      atualizado_em = now()
  where id_ordem = target_id_ordem
    and status_codigo <> 'cancelada';
end;
$$;

create or replace function private.recalcular_status_ordem_trigger()
returns trigger
language plpgsql
security definer
set search_path = ''
as $$
declare
  target_id integer;
begin
  if tg_table_name = 'pendencias_ordem_manutencao' then
    target_id := coalesce(new.id_ordem, old.id_ordem);
  else
    select pom.id_ordem
    into target_id
    from public.pendencias_ordem_manutencao pom
    where pom.id_pendencia_ordem = coalesce(new.id_pendencia_ordem, old.id_pendencia_ordem);
  end if;

  perform private.recalcular_status_ordem(target_id);
  return coalesce(new, old);
end;
$$;

create or replace function private.sync_pendencia_status_from_execucao()
returns trigger
language plpgsql
security definer
set search_path = ''
as $$
begin
  if new.status_codigo = 'concluida' then
    update public.pendencias_ordem_manutencao
    set status_codigo = 'concluida',
        atualizado_em = now()
    where id_pendencia_ordem = new.id_pendencia_ordem;
  elsif new.status_codigo = 'em_tratamento' then
    update public.pendencias_ordem_manutencao
    set status_codigo = 'em_tratamento',
        atualizado_em = now()
    where id_pendencia_ordem = new.id_pendencia_ordem
      and status_codigo <> 'concluida';
  end if;

  return new;
end;
$$;

create or replace function private.recalcular_estoque_material(target_id_material integer)
returns void
language sql
security definer
set search_path = ''
as $$
  update public.estoque_materiais em
  set estoque_atual = coalesce((
        select sum(me.quantidade * tm.fator)
        from public.movimentacoes_estoque me
        join public.movimentacoes m on m.id_movimentacao = me.id_movimentacao
        join public.tipos_movimentacao tm on tm.codigo = m.tipo_codigo
        where me.id_material = target_id_material
      ), 0),
      atualizado_em = now()
  where em.id_material = target_id_material;
$$;

create or replace function private.recalcular_estoque_trigger()
returns trigger
language plpgsql
security definer
set search_path = ''
as $$
begin
  perform private.recalcular_estoque_material(coalesce(new.id_material, old.id_material));
  return coalesce(new, old);
end;
$$;

create or replace function public.criar_ordem_manutencao(
  p_id_unidade integer,
  p_protocolo text,
  p_tipo text,
  p_prioridade text,
  p_prazo_sla timestamptz,
  p_descricao text,
  p_pendencias integer[]
)
returns public.ordens_manutencao
language plpgsql
security invoker
set search_path = ''
as $$
declare
  created_order public.ordens_manutencao;
begin
  insert into public.ordens_manutencao (
    id_unidade,
    protocolo,
    tipo,
    prioridade,
    prazo_sla,
    descricao,
    criado_por
  )
  values (
    p_id_unidade,
    p_protocolo,
    coalesce(nullif(p_tipo, ''), 'corretiva'),
    coalesce(nullif(p_prioridade, ''), 'P3'),
    p_prazo_sla,
    coalesce(p_descricao, ''),
    auth.uid()
  )
  returning * into created_order;

  insert into public.pendencias_ordem_manutencao (
    id_ordem,
    id_pendencia,
    status_codigo,
    descricao_snapshot
  )
  select
    created_order.id_ordem,
    pp.id_pendencia,
    'aberta',
    pp.descricao
  from public.pendencias_padrao pp
  where pp.id_pendencia = any(coalesce(p_pendencias, '{}'))
    and pp.ativo is true
  on conflict (id_ordem, id_pendencia) do nothing;

  select *
  into created_order
  from public.ordens_manutencao
  where id_ordem = created_order.id_ordem;

  return created_order;
end;
$$;

