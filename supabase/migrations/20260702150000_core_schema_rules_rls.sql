declare
  table_name text;
begin
  foreach table_name in array array[
    'clientes', 'empresas', 'contratos', 'projetos', 'unidades_instaladas',
    'colaboradores', 'terceirizados', 'estoque_materiais', 'pendencias_padrao',
    'ordens_manutencao', 'pendencias_ordem_manutencao', 'atendimentos_ordens',
    'equipes', 'atendimentos_executados', 'movimentacoes'
  ] loop
    execute format('drop trigger if exists set_%I_updated_at on public.%I', table_name, table_name);
    execute format(
      'create trigger set_%I_updated_at before update on public.%I for each row execute function public.set_atualizado_em()',
      table_name,
      table_name
    );
  end loop;
end $$;

